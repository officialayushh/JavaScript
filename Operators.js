// Arithmetic operators
var num_1 = 5;
var num_2 = 2;
console.log("The value of num_1 and num_2 is"+ " " + (num_1+num_2));
console.log("The value of num_1 and num_2 is"+ " " + (num_1-num_2));
console.log("The value of num_1 and num_2 is"+ " " + (num_1*num_2));
console.log("The value of num_1 and num_2 is"+ " " + (num_1/num_2));
console.log("The value of num_1 and num_2 is"+ " " + (num_1%num_2));

// Exponential operators
console.log("Square of this is" + " " + num_2**num_2);
console.log("Square of this is" + " " + num_1**num_2);

// Increment or decrement operators
console.log("The value of num_1 is"+ " " + (num_1++));
console.log("The value of num_1 is"+ " " + (++num_1));
